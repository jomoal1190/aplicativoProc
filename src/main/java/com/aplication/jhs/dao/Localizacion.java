package com.aplication.jhs.dao;

public class Localizacion {
	
	private String token;
	private String latitutd;
	private String longitud;
	private Long idAsociacion;
	
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getLatitutd() {
		return latitutd;
	}
	public void setLatitutd(String latitutd) {
		this.latitutd = latitutd;
	}
	public String getLongitud() {
		return longitud;
	}
	public void setLongitud(String longitud) {
		this.longitud = longitud;
	}
	public Long getIdAsociacion() {
		return idAsociacion;
	}
	public void setIdAsociacion(Long idAsociacion) {
		this.idAsociacion = idAsociacion;
	}
	
	

}
